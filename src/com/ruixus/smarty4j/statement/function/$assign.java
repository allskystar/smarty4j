package com.ruixus.smarty4j.statement.function;

import com.ruixus.smarty4j.MethodVisitorProxy;
import com.ruixus.smarty4j.VariableManager;
import com.ruixus.smarty4j.statement.Definition;
import com.ruixus.smarty4j.statement.Definition.Type;
import com.ruixus.smarty4j.statement.Function;

/**
 * This is used for assigning template variables during the execution of a template.
 * 
 * <table border="1">
 * <colgroup> <col align="center" class="param"> <col align="center" class="type"> <col
 * align="center" class="required"> <col align="center" class="default"> <col class="desc">
 * </colgroup> <thead>
 * <tr>
 * <th align="center">Attribute Name</th>
 * <th align="center">Type</th>
 * <th align="center">Required</th>
 * <th align="center">Default</th>
 * <th>Description</th>
 * </tr>
 * </thead> <tbody>
 * <tr>
 * <td align="center">var</td>
 * <td align="center">string</td>
 * <td align="center">Yes</td>
 * <td align="center"><span class="emphasis"><em>n/a</em></span></td>
 * <td>The name of the variable being assigned</td>
 * </tr>
 * <tr>
 * <td align="center">value</td>
 * <td align="center">string</td>
 * <td align="center">Yes</td>
 * <td align="center"><span class="emphasis"><em>n/a</em></span></td>
 * <td>The value being assigned</td>
 * </tr>
 * </tbody>
 * </table>
 * 
 * @version 1.1.0, 2015/05/16
 * @author Ouyang Xianwei
 * @since Smarty 1.1
 */
public class $assign extends Function {

	private static final Definition[] definitions = {
	    Definition.forFunction("var", Type.STRING),
	    Definition.forFunction("value", Type.OBJECT) };

	@Override
	public Definition[] getDefinitions() {
		return definitions;
	}

	@Override
	public void parse(MethodVisitorProxy mv, int local, VariableManager vm) {
		writeVariable(mv, local, vm, PARAMETERS[0].toString(), new VariableLoader() {
			public void parse(MethodVisitorProxy mv, int local, VariableManager vm) {
				PARAMETERS[1].parseObject(mv, local, vm);
			}
		});
	}
}